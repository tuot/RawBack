let currentLanguage = 'en';

function normalizedLanguage(language) {
    const normalized = language.replace('-', '_');
    if (translations[normalized]) return normalized;

    const baseLanguage = normalized.split('_')[0];
    if (baseLanguage === 'zh') return 'zh_CN';
    if (translations[baseLanguage]) return baseLanguage;

    return null;
}

function preferredLanguage() {
    const languages = navigator.languages && navigator.languages.length > 0
        ? navigator.languages
        : [navigator.language || 'en'];

    for (const language of languages) {
        const match = normalizedLanguage(language);
        if (match) return match;
    }

    return 'en';
}

function t(key) {
    return translations[currentLanguage][key] || translations.en[key] || key;
}

function applyTranslations() {
    const language = preferredLanguage();
    currentLanguage = language;
    document.documentElement.lang = language.replace('_', '-');

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        element.textContent = t(element.dataset.i18n);
    });
}

function setText(selector, key) {
    const element = document.querySelector(selector);
    if (element) element.textContent = t(key);
}

function revealWhenLayoutSettled() {
    document.body.classList.remove('ready');
    requestAnimationFrame(() => {
        requestAnimationFrame(() => {
            document.body.classList.add('ready');
        });
    });
}

function show(platform, enabled, useSettingsInsteadOfPreferences) {
    applyTranslations();

    document.body.classList.toggle('platform-ios', platform === 'ios');
    document.body.classList.toggle('platform-mac', platform === 'mac');
    document.body.classList.remove('state-on', 'state-off', 'state-unknown');

    if (platform === 'mac') {
        if (typeof enabled === 'boolean') {
            document.body.classList.toggle('state-on', enabled);
            document.body.classList.toggle('state-off', !enabled);
        } else {
            document.body.classList.add('state-unknown');
        }

        setText(
            '.open-preferences',
            useSettingsInsteadOfPreferences ? 'btn-open-settings' : 'btn-open-preferences',
        );
    }

    revealWhenLayoutSettled();
}

function openPreferences() {
    webkit.messageHandlers.controller.postMessage('open-preferences');
}

applyTranslations();
document.querySelector('button.open-preferences')?.addEventListener('click', openPreferences);
